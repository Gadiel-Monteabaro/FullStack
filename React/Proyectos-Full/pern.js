/*
  PERN   
    PostgreSQL Express React Node.js

    Un stack es un conjunto de Herramientas para crear una app

    React en el Front End y Node en el backend son una combinación muy común
*/
/*
  PostgreSQL
    Es un sistema de gestión de bases de datos relacional orientado a objetos y de código abierto

    Para interactuar con la bse de datos podemos hacerlo por medio de un ORM

    Un ORM tiene todos los métodos para crear, obtener, actualizar y eliminar datos de nuestra base de datos
*/
/*
  Express   
    Infraestructura web rápida, minimalista y flexible para Node.js

    A diferencia de otros Frameworks no tiene un sistema de vistas definido, tampoco tiene ORM o Autenticación, la configuración la deja en tus manos

    Ideal para Aplicaciones Web Monolíticas o como API
*/
/*
  Node.js
    Entorno de ejcución en JavaScript que se ejecuta en el servidor

    Cantidad de librerias disponibles para integrarse en proyectos con NPM

    Puede consultar Base de Datos, Autenticar Usuarios, manejar rutas, ...
*/
